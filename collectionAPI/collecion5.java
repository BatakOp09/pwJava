package collectionAPI;

import java.util.*;
public class collecion5 {
    public static void main(String args[]){

        HashSet hs = new HashSet();
        hs.add(100);
        hs.add(20);
        hs.add(80);
        hs.add(70);
    
        System.out.println(hs);

        LinkedHashSet hs1 = new LinkedHashSet();
        hs1.add(100);
        hs1.add(20);
        hs1.add(80);
        hs1.add(70);

        System.out.println(hs1);


    }
}
